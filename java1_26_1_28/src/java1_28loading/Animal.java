package java1_28loading;

public class Animal{
	// 体長と速度の値をセットする用
	private double width;
	private int speed;
	
	// getter setter
	public double getWidth() {
		return width;
	}

	public int getSpeed() {
		return speed;
	}

	public void setWidth(double width) {
		this.width = width;
	}
	
	public void setSpeed(int speed) {
		this.speed = speed;
	}
}